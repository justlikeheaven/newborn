				isset($aValue[$value]) && ($aValue[$key] == '') && ($aTemp[$value] = $aValue[$key]);
			}else{
				($aValue[$key] !== null) && ($aValue[$key] !== '') && ($aTemp[$value] = $aValue[$key]);
			}
		}
		return (array) $aTemp;
	}
